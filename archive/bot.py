def load_strategy(strategy_name):
    module_name = AVAILABLE_STRATEGIES.get(strategy_name)
    if not module_name:
        available = ", ".join(sorted(AVAILABLE_STRATEGIES))
        raise ValueError(
            f"Unsupported strategy '{strategy_name}'. Available strategies: {available}"
        )

    return importlib.import_module(module_name)


def connect():
    if mt5.initialize():
        print("Connected to MT5")
        return True

    print(f"Failed to connect to MT5. Error: {mt5.last_error()}")
    return False


def fetch_data(symbol, timeframe, bars=500):
    mt5_timeframe = TIMEFRAME_MAP.get(timeframe)
    if mt5_timeframe is None:
        valid = ", ".join(TIMEFRAME_MAP)
        raise ValueError(f"Invalid timeframe '{timeframe}'. Valid values: {valid}")

    rates = mt5.copy_rates_from_pos(symbol, mt5_timeframe, 0, bars)
    if rates is None or len(rates) == 0:
        print(f"No data received for {symbol} on {timeframe}")
        return None

    df = pd.DataFrame(rates)
    df.columns = [
        "time",
        "open",
        "high",
        "low",
        "close",
        "tick_volume",
        "spread",
        "real_volume",
    ]
    df["time"] = pd.to_datetime(df["time"], unit="s")
    return df


def analyze_timeframe(module, pair, timeframe, bars):
    raw_df = fetch_data(pair, timeframe, bars=bars)
    if raw_df is None or len(raw_df) < 3:
        return None

    closed_df = raw_df.iloc[:-1].copy()
    analyzed_df = module.calculate_indicators(closed_df)

    if hasattr(module, "analyze_setup"):
        analysis = module.analyze_setup(analyzed_df)
    else:
        signal = module.generate_signal(analyzed_df)
        analysis = {
            "signal": signal,
            "reason": "Signal generated by strategy module.",
            "checks": {},
        }

    latest_closed_candle = analyzed_df.iloc[-1][["time", "open", "high", "low", "close"]]
    analysis["latest_closed_candle"] = latest_closed_candle
    analysis["next_open_candle"] = raw_df.iloc[-1][["time", "open", "high", "low", "close"]]
    analysis["timeframe"] = timeframe
    return analysis


def format_check(name, value):
    if isinstance(value, bool):
        return f"  - {name}: {'PASS' if value else 'FAIL'}"
    return f"  - {name}: {value}"


def determine_higher_timeframe_bias(trend_analysis):
    checks = trend_analysis.get("checks", {})
    trend_bias = checks.get("trend_bias", "NEUTRAL")
    market_choppy = checks.get("market_choppy", True)
    timeframe = trend_analysis.get("timeframe", "higher timeframe")

    if trend_bias == "STRONG_BUY":
        return "BUY", "strong", f"{timeframe} strong bullish bias: EMA stack and 200 EMA slope agree."
    if trend_bias == "STRONG_SELL":
        return "SELL", "strong", f"{timeframe} strong bearish bias: EMA stack and 200 EMA slope agree."
    if trend_bias == "WEAK_BUY":
        return "BUY", "weak", f"{timeframe} weak bullish bias: price is still supported above the 200 EMA."
    if trend_bias == "WEAK_SELL":
        return "SELL", "weak", f"{timeframe} weak bearish bias: price is still contained below the 200 EMA."
    if market_choppy:
        return "NEUTRAL", "neutral", f"{timeframe} trend is choppy, so no entry bias is allowed."
    return "NEUTRAL", "neutral", f"{timeframe} trend is unclear."


def combine_analyses(trend_analysis, entry_analysis):
    higher_bias, bias_strength, bias_reason = determine_higher_timeframe_bias(trend_analysis)
    entry_signal = entry_analysis["signal"]
    entry_reason = entry_analysis["reason"]

    if higher_bias == "NEUTRAL":
        final_signal = "WAIT"
        final_reason = f"No trade: {bias_reason}"
    elif entry_signal == "WAIT":
        final_signal = "WAIT"
        final_reason = (
            f"No trade: higher timeframe {bias_strength} bias agrees to look for {higher_bias}, "
            f"but entry timeframe says WAIT. {entry_reason}"
        )
    elif entry_signal != higher_bias:
        final_signal = "WAIT"
        final_reason = (
            f"No trade: entry timeframe shows {entry_signal}, but it conflicts with the higher timeframe {higher_bias} bias."
        )
    else:
        final_signal = entry_signal
        final_reason = (
            f"{entry_signal} setup confirmed: higher timeframe bias and entry timeframe signal align."
        )

    return {
        "signal": final_signal,
        "reason": final_reason,
        "higher_bias": higher_bias,
        "bias_strength": bias_strength,
        "bias_reason": bias_reason,
    }



def run_once(
    module,
    strategy_name,
    pair,
    trend_timeframe,
    entry_timeframe,
    bars,
    session_state,
    max_signals_per_day,
):
    trend_analysis = analyze_timeframe(module, pair, trend_timeframe, bars)
    entry_analysis = analyze_timeframe(module, pair, entry_timeframe, bars)

    if trend_analysis is None or entry_analysis is None:
        print("Unable to fetch market data.")
        return False

    combined = combine_analyses(trend_analysis, entry_analysis)
    combined["trend_analysis"] = trend_analysis
    combined["entry_analysis"] = entry_analysis
    combined["trade_plan"] = None

    if combined["signal"] in {"BUY", "SELL"}:
        atr = entry_analysis.get("checks", {}).get("atr")
        projected_entry = entry_analysis["next_open_candle"]["open"]
        if atr:
            combined["trade_plan"] = build_trade_plan(
                combined["signal"],
                projected_entry,
                float(atr),
                START_BALANCE,
                tp_r=TP_R,
            )

    entry_candle_time = entry_analysis["latest_closed_candle"]["time"]
    session_date = datetime.now().date().isoformat()
    if session_state["date"] != session_date:
        session_state["date"] = session_date
        session_state["signals_today"] = 0
        session_state["last_entry_candle"] = None

    if session_state["last_entry_candle"] == entry_candle_time:
        return True

    if (
        session_state["signals_today"] >= max_signals_per_day
        and combined["signal"] in {"BUY", "SELL"}
    ):
        combined["reason"] = (
            f"Signal blocked: daily trade limit of {max_signals_per_day} reached. "
            f"Original setup was {combined['signal']}."
        )
        combined["signal"] = "WAIT"

    if combined["signal"] in {"BUY", "SELL"}:
        session_state["signals_today"] += 1

    session_state["last_entry_candle"] = entry_candle_time
    print_analysis(
        combined,
        pair,
        strategy_name,
        max_signals_per_day,
        session_state["signals_today"],
    )
    return True


def parse_args():
    parser = argparse.ArgumentParser(description="Run the Deriv trading bot.")
    parser.add_argument(
        "--strategy",
        default=DEFAULT_STRATEGY,
        help=f"Strategy name. Default: {DEFAULT_STRATEGY}",
    )
    parser.add_argument(
        "--pair",
        default=DEFAULT_PAIR,
        help=f"Market symbol. Default: {DEFAULT_PAIR}",
    )
    parser.add_argument(
        "--trend-timeframe",
        default=DEFAULT_TREND_TIMEFRAME,
        help=f"Higher timeframe for market bias. Default: {DEFAULT_TREND_TIMEFRAME}",
    )
    parser.add_argument(
        "--entry-timeframe",
        default=DEFAULT_ENTRY_TIMEFRAME,
        help=f"Lower timeframe for entries. Default: {DEFAULT_ENTRY_TIMEFRAME}",
    )
    parser.add_argument(
        "--bars",
        type=int,
        default=500,
        help="Number of candles to fetch. Default: 500",
    )
    parser.add_argument(
        "--watch",
        action="store_true",
        help="Keep polling for new signals instead of running once.",
    )
    parser.add_argument(
        "--interval",
        type=int,
        default=60,
        help="Polling interval in seconds when using --watch. Default: 60",
    )
    parser.add_argument(
        "--max-signals",
        type=int,
        default=DEFAULT_MAX_SIGNALS_PER_DAY,
        help="Maximum BUY/SELL signals allowed per day. Default: 3",
    )
    return parser.parse_args()


def main():
    args = parse_args()
    module = load_strategy(args.strategy)
    session_state = {"date": None, "signals_today": 0, "last_entry_candle": None}

    print("Starting Trading Bot...")
    print(
        f"Rule card mode: {args.trend_timeframe} trend bias + "
        f"{args.entry_timeframe} pullback and confirmation entry"
    )

    if not connect():
        return

    try:
        if args.watch:
            print(
                f"Watching {args.pair} | trend={args.trend_timeframe} | "
                f"entry={args.entry_timeframe} | every {args.interval}s | "
                f"max daily signals: {args.max_signals}"
            )
            while True:
                run_once(
                    module,
                    args.strategy,
                    args.pair,
                    args.trend_timeframe,
                    args.entry_timeframe,
                    args.bars,
                    session_state,
                    args.max_signals,
                )
                time.sleep(args.interval)
        else:
            run_once(
                module,
                args.strategy,
                args.pair,
                args.trend_timeframe,
                args.entry_timeframe,
                args.bars,
                session_state,
                args.max_signals,
            )
    finally:
        mt5.shutdown()
        print("MT5 connection closed.")


if __name__ == "__main__":
    main()
